#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*dest;
	int		lentsrc;
	int		i;

	lentsrc = ft_strlen(src) + 1;
	dest = (char *)malloc(lentsrc);
	if (!dest)
		return (NULL);
	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
